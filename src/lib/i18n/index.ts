/**
 * Lấy văn bản theo ngôn ngữ chỉ định (Mặc định: Tiếng Việt 'vi')
 */
export function t(key: string, lang: SupportedLanguage = 'vi', params?: Record<string, string>): string {
  const dict = i18nDict[lang] || i18nDict.vi;
  let text = dict[key] || i18nDict.vi[key] || key;

  if (params) {
    Object.keys(params).forEach((paramKey) => {
      text = text.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), params[paramKey]);
    });
  }

  return text;
}

export function isValidLanguage(lang: string): lang is SupportedLanguage {
  return [
    'vi',
    'en',
    'zh',
    'ja',
    'ko',
    'th',
    'ms',
    'id',
    'es',
    'fr',
    'de',
    'ru',
    'pt',
    'it',
    'ar',
    'hi',
  ].includes(lang);
}
